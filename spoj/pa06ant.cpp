struct matrix
{
	long long mat[32][32]; 
};
int mod;
matrix mult(matrix a , matrix b1)
{
	matrix ans;
	int x;
	for(int i = 0; i < 32; i++)
	{
		for(int j = 0; j < 32; j++)
		{
			for(ans.mat[i][j] = x = 0; x < 32; x++)
				ans.mat[i][j] = (ans.mat[i][j] + (a.mat[i][x] * b1.mat[x][j]) % mod) %mod;
		}
	}
	return ans;
}
matrix power(matrix base, int p)
{
	matrix ans;
	for(int i = 0; i < 32; i++)
	{
		for(int j = 0; j < 32; j++)
		{
			if(i == j)
				ans.mat[i][j] = 1LL;
			else
				ans.mat[i][j] = 0LL;
		}
	}
	if(p == 0) return ans;
	if(p == 1) return base;
	while(p)
	{
		if(p & 1)
			ans = mult(ans,base);
		base = mult(base,base);
		p >>= 1;
	}
	return ans;
}
